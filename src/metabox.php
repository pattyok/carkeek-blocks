<?php

function carkeek_blocks_register_meta() {
    register_meta('post', '_carkeek_blocks_post_subtitle', array(
        'show_in_rest' => true,
        'type' => 'string',
        'single' => true,
        'sanitize_callback' => 'sanitize_text_field',
        'auth_callback' => function() {
            return current_user_can('edit_posts');
        }
    ));
}

